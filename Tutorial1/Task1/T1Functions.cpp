/// <summary>
/// Calculates the area of a given rectangle
/// </summary>
/// <param name="width">Rectangle Width</param>
/// <param name="height">Rectangle Height</param>
int CalculateArea(const int width, const int height)
{
	return width * height;
}

/// <summary>
/// Calculates perimeter of a given rectangle
/// </summary>
/// <param name="width">Rectangle Width</param>
/// <param name="height">Rectangle Height</param>
int CalculatePerimeter(const int width, const int height)
{
	return 2 * (width + height);
}